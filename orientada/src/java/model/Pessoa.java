package model;

/**
 *
 * @author 42165
 */
public class Pessoa {
    private Integer codigo;
    private String nome;
    private Integer idade;
    private String cpf;
    
    public Pessoa(Integer codigo, String nome, Integer idade, String cpf) {
        this.codigo = codigo;
        this.nome = nome;
        this.idade = idade;
        this.cpf = cpf;
    }

    public Integer getCodigo() {
        return codigo;
    }

    public void setCodigo(Integer codigo) {
        this.codigo = codigo;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public Integer getIdade() {
        return idade;
    }

    public void setIdade(Integer idade) {
        this.idade = idade;
    }

    public String getCpf() {
        return cpf;
    }

    public void setCpf(String cpf) {
        this.cpf = cpf;
    }   
    
}
